// Generate flat list of all locations with province info
export const getAllLocations = (): Location[] => {
  const locations: Location[] = [];
  
  provinces.forEach(province => {
    province.locations.forEach(loc => {
      locations.push({
        ...loc,
        province: province.name,
        provinceSlug: province.slug
      });
    });
  });
  
  return locations;
};

// Get location by slug
export const getLocationBySlug = (slug: string): Location | undefined => {
  return getAllLocations().find(loc => loc.slug === slug);
};

// Get all locations in a province
export const getLocationsByProvince = (provinceSlug: string): Location[] => {
  return getAllLocations().filter(loc => loc.provinceSlug === provinceSlug);
};

// Get province by slug
export const getProvinceBySlug = (slug: string): Province | undefined => {
  return provinces.find(p => p.slug === slug);
};

// Get nearby locations (simple alphabetical for now)
export const getNearbyLocations = (slug: string, count: number = 6): Location[] => {
  const allLocations = getAllLocations();
  const currentLocation = getLocationBySlug(slug);
  
  if (!currentLocation) return allLocations.slice(0, count);
  
  // Get locations from the same province first
  const sameProvince = allLocations.filter(
    loc => loc.provinceSlug === currentLocation.provinceSlug && loc.slug !== slug
  );
  
  return sameProvince.slice(0, count);
};

// Total count for display
export const getTotalLocationCount = (): number => {
  return getAllLocations().length;
};
